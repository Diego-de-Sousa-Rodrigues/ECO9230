function [lhs, rhs] = static_resid(y, x, params)
T = NaN(2, 1);
lhs = NaN(8, 1);
rhs = NaN(8, 1);
T(1) = 1/params(4)-(1-params(5));
T(2) = (T(1)/(T(1)-params(5)*params(3)))^(params(1)/(params(1)+params(2)))*((1-params(3))^(-params(2))*((1-params(3))*(params(3)/T(1))^(params(3)/(1-params(3))))^(1+params(2)))^(1/(params(1)+params(2)));
lhs(1) = params(1)*y(3)+params(2)*y(7);
rhs(1) = y(6);
lhs(2) = 0;
rhs(2) = T(1)*y(4);
lhs(3) = y(5);
rhs(3) = (1-params(5))*y(5)+params(5)*y(2);
lhs(4) = y(1);
rhs(4) = y(8)+params(3)*y(5)+(1-params(3))*y(7);
lhs(5) = y(4);
rhs(5) = y(1)-y(5);
lhs(6) = y(6);
rhs(6) = y(1)-y(7);
lhs(7) = T(2)*y(1);
rhs(7) = (T(2)-T(2)*params(5)*params(3)/T(1))*y(3)+T(2)*params(5)*params(3)/T(1)*y(2);
lhs(8) = y(8);
rhs(8) = y(8)*params(6)+x(1);
end
