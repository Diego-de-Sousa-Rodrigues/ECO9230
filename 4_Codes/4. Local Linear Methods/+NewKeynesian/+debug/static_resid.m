function [lhs, rhs] = static_resid(y, x, params)
T = NaN(7, 1);
lhs = NaN(11, 1);
rhs = NaN(11, 1);
T(1) = 1/params(4)-(1-params(5));
T(2) = (params(7)-1)/params(7)*(1-params(4)*params(8));
T(3) = (1-params(3))*T(2)^(1/(1-params(3)))*(params(3)/T(1))^(params(3)/(1-params(3)));
T(4) = T(3)*(T(3)/(T(2)*(1-params(3))))^params(2);
T(5) = (T(1)/(T(1)-T(2)*params(5)*params(3)))^(params(1)/(params(1)+params(2)))*T(4)^(1/(params(1)+params(2)));
T(6) = 1/T(5)^(params(2)/params(1))*T(4)^(1/params(1));
T(7) = (1-params(4)*params(8))*(1-params(8))/params(8);
lhs(1) = params(1)*y(3)+params(2)*y(7);
rhs(1) = y(6)-y(9);
lhs(2) = 0;
rhs(2) = T(1)*(y(4)-y(9));
lhs(3) = y(5);
rhs(3) = (1-params(5))*y(5)+params(5)*y(2);
lhs(4) = y(1);
rhs(4) = y(11)+params(3)*y(5)+(1-params(3))*y(7);
lhs(5) = y(5);
rhs(5) = y(1)-y(4);
lhs(6) = y(7);
rhs(6) = y(1)-y(6);
lhs(7) = y(8);
rhs(7) = (1-params(3))*y(6)+params(3)*y(4)-y(11);
lhs(8) = y(10);
rhs(8) = params(4)*y(10)+T(7)*(y(8)-y(9));
lhs(9) = y(10);
rhs(9) = 0;
lhs(10) = T(5)*y(1);
rhs(10) = T(6)*y(3)+T(5)*T(2)*params(5)*params(3)/T(1)*y(2);
lhs(11) = y(11);
rhs(11) = y(11)*params(6)+x(1);
end
